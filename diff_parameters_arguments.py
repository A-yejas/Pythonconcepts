'''
Parameters:-
Definition:- Parameters are the variables listed inside the parentheses in the function definition.
Role:- They act as placeholders for the values that will be passed into the function when it is called.
Location:- Parameters are defined in the function signature (the line where the function is declared).
Example:-
def greet(name, age):  # 'name' and 'age' are parameters
    print(f"Hello, my name is {name} and I am {age} years old.")
------------------------------------------------------------------------------------------
Arguments
Definition: Arguments are the actual values that are passed to the function when it is called.
Role: They provide the data to the function's parameters when the function is executed.
Location: Arguments are supplied in the function call.
Example:-
greet("Alice", 30)  # 'Alice' and 30 are arguments
---------
Summary:
Parameters are variables in a function definition, acting as placeholders.
Arguments are the actual values passed to the function when it is invoked.

'''
